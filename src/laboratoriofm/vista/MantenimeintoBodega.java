    public void tabla() {

        modelo1 = new DefaultTableModel();
        modelo1.addColumn("ID");
        modelo1.addColumn("Nombre");
        modelo1.addColumn("Autor");
        modelo1.addColumn("Genero");
        modelo1.addColumn("Tipo");
        modelo1.addColumn("Cantidad");

        jTable1.setModel(modelo1);

        String datos[] = new String[6];
        BodegaDAO habitacionesDAO = new BodegaDAO();
        List<Bodega> habitaciones = habitacionesDAO.select();
        for (Bodega habitacion : habitaciones) {
            datos[0] = String.valueOf(habitacion.getId_Bodega());
            datos[1] = String.valueOf(habitacion.getNombre_Art());
            datos[2] = String.valueOf(habitacion.getAutor_Art());
            datos[3] = String.valueOf(habitacion.getGenero());
            datos[4] = String.valueOf(habitacion.getTipo_Art());
            datos[5] = String.valueOf(habitacion.getCantidad_Art());

            modelo1.addRow(datos);
            jTable1.setModel(modelo1);

        }
    }

